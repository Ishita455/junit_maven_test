package com.mycompany.calculatorapp;

public class Calculator {

    public Double add(Double a, Double b){
        Double result = a + b;
        return  result;
    }

    public Double sub(Double a , Double b){
        Double result = a - b;
        return result;
    }

    public Double mult(Double a, Double b){
        Double result = a * b;
        return result;
    }

    public Double div(Double a, Double b){
        Double result = a / b;
        return result;
    }
}
